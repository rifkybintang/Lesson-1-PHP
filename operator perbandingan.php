<?php

// istilah tipe jugling auto konversi tipe data jika a integer b string
// a == b (sama dengan)

// kalau gak mau pakek konversi atau tipe data udah sama berarti pakek identik aja
// a === b (identik)

//setelah di konversi kalau value tidak sama berarti true ada juglingnya
// a != b (tidak sama dengan)

// aslinya sama cuman pakek yang atas aja dah yang umum
// a <> b (tidak sama dengan)

// ini sama kaya yang (===) cuman versi tidak identiknya
// a !== b (tidak identik)

// bernilai benar kalau kurang dari
// a < b (kurang dari)

// nilai true jika a kurang dari b atau sama dengan b
// a <= b (kurang dari sama dengan)

// lebih dari jika a lebih bernilai true
// a > b (lebih dari)

// nilai true jika a lebih dari b atau sama dengan b
// a >= b (lebih dari sama dengan)

//bandingin pakek var_dump()
var_dump("10" == 10); //jugling
var_dump("10" === 10);

var_dump(10 > 9);
var_dump(10 >= 10);

